            Цель: определить взаимосвязь между изначальной длиной теломер и развитием рака и смертности от рака
<br><br>
			Продолжительность исследования: с 1995 по 2005 год.<br>
			Участники: 787 добровольцев, не имеющих рака в возрасте от 40 до 79 лет.<br>
			Дизайн: в начале исследования была измерена длина теломер лейкоцитов методом ПЦР, затем в течение 10 лет отслеживалась заболеваемость раком 
			и смертность от него.<br>
			Результаты: За годы исследования рак был выявлен у 11,7% добровольцев.<br>
			Средняя длина теломер у пациентов с раком оказалась значительно меньше, чем у остальных участников исследования.
<br><br>
			Главные итоговые выводы:<br>
			Лица, имеющие самые короткие теломеры, в 3 раза больше рискуют заболеть раком и в 11 раз больше – умереть от него в 10-летний период по 
			сравнению с теми, у кого длина теломер максимальна.<br>
			Лица со средней длиной теломер имеют риск рака вдвое выше, чем лица с наиболее длинными теломерами. Наличие коротких теломер чаще связано 
			с наиболее злокачественными опухолями, такими как рак желудка, легких и яичников.<br>
