            Цель исследования: подтвердить механизм действия, выявить долгосрочные эффекты и оценить безопасность ТА-65.<br>
			<br>
			Модель исследования: Для подтверждения механизма действия, основанного на активации теломеразы, TA-65® давали в течение 4-х месяцев в 
			дозе 10 мкм 2 группам специально выращенных мышей с дефектным и нормальным геном теломеразы. В начале исследования в обеих группах было 
			подтверждено наличие слишком коротких теломер.<br>
			Предполагаемая гипотеза: если механизм действия TA-65® действительно активация теломеразы, то эффект лечения должен проявиться только 
			в группе с действующим геном, в группе с дефектным геном лечебный эффект должен отсутствовать. <br>
			<br>
			Результаты:<br>
			1. Снижение процентного содержания коротких теломер в группе мышей с активным геном теломеразы и отсутствие такового эффекта в группе мышей 
			с дефектным геном подтвердило эффективность действие ТА-65 на активацию теломеразы, а также показало, что TA-65® «предпочитает» активировать 
			теломеразу, прежде всего, в клетках с наиболее короткими теломерами.<br>
			2. В группе мышей с активным геном теломеразы применение TA-65® не влияло на среднюю или максимальную продолжительность жизни, но значительно 
			повышало продолжительность здоровой жизни, что выражалось в улучшении следующих биологических показателей процесса старения:<br>
			нормальный метаболизм глюкозы;<br>
			нормальное состояние и функция печени;<br>
			здоровое отрастание волос;<br>
			нормальное и непрерывное клеточное восстановление и омоложение;<br>
			нормальная плотность костной ткани;<br>
			нормальные гематологические показатели.<br>
			<br>
			Данные о безопасности: Побочные эффекты не наблюдались. Применение TA-65® не вызывало нерегулируемый рост клеток. Это подтверждает, 
			что TA-65® является безопасной биологической субстанцией, оказывающей временное и щадящее влияние на активацию теломеразы в здоровых клетках.<br>
