            Цель исследования:<br>
			• Оценить клинические эффекты приема ТА-65<br>
			<br>
			Материалы и методы:<br>
			• 114 человек; возраст – 63 ± 12 лет; возрастной диапазон: 30 – 87 лет; 72% – мужчины, 54% участников ЦМВ-сероположительные<br>
			• Все участники принимали ТА-65<br>
			• определяли длину теломер и другие биологические показатели процесса старения (анализ крови, состояние и работу иммунной системы, наличие 
			цитомегаловируса, состояние почек, печени, работу эндокринной системы, уровень холестерина, метаболизм глюкозы, состояние сердечно-сосудистой 
			системы, костей, кожи, зрения).<br>
			• длину теломер в периферических лимфоцитах и гранулоцитах определяли методом FlowFISH в лаборатории RepeatDiagnostics (Ванкувер, Канада).<br>
			• Оценку проводили до начала исследования, через 3, 6, 9 и 12 месяцев.<br>
			<br>
			Результаты и выводы:<br>
			За 12 мес приема ТА-65 у пациентов отмечалось:<br>
			· Статистически значимое снижение процента коротких теломер (р = 0,037).<br>
			· Снижение процента стареющих цитотоксических (CD8+/CD28-) Т-клеток (на 1,5 %; 4,4 %; 8,6 % и 7,5% через 3, 6, 9 и 12 месяцев соответственно у 
			лиц, инфицированных цитомегаловирусом).<br>
			· Улучшение показателей липидного и углеводного обмена: глюкозы натощак: -3,72 мг / дл (р = 0,02), инсулина -1,32 мМЕ / мл (р = 0,01), общего 
			холестерина- 13.2 8 мг / дл (р = 0,002), липопротеинов низкой плотности (LDL-C) -11.8 мг / дл ( р = 0,002 ), гомоцистеина - 3,6 мкмоль / л (р = 0,001). 
			Для систолического и диастолического артериального давления динамика составила -17,3 и -4,2 мм рт.ст. (р = 0,007 и 0,001, соответственно).<br>
			· Через 12 месяцев, минеральная плотность кости увеличилась на 2,0% (р = 0,003). Приём ТА-65 не был ассоциирован с развитием побочных эффектов.<br>
			<br>
			Участники исследования также сообщили о других положительных эффектах: улучшение зрения, половой функции, повышение уровня энергии и выносливости, 
			гибкости, остроты мышления и нормализация веса. Кроме того, сообщали об уменьшении появления возрастных пигментных пятен и об улучшении общего 
			состояния кожи, волос и ногтей, что требует дополнительного изучения.<br>
