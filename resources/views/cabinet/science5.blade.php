                <span style="font-size: 16px;">
                    <font><b>
                    2. Повышение противовирусной функции человеческих CD8 + T-лимфоцитов как результат фармакологической активации теломеразы</b><br>
					здоровых доноров, носителей ВИЧ-инфекции и больных СПИДом.
                    </font>
