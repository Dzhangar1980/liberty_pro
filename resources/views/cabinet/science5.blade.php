            <span style="font-size: 14px;">
            Цель: оценить эффективность воздействия ТА-65 на активность теломеразы и пролиферативный потенциал человеческих CD4 и CD8 Т-клеток.
			<br><br>
			Дизайн исследования: CD4 и CD8 Т-лимфоциты здоровых пациентов подвергли воздействию TA-65®. Через 72 часа после первичной стимуляции измеряли активность теломеразы в образцах. В качестве второго стимулирования этот процесс повторили через 18 – 21 день.
			<br>
			Результаты:<br>
			• Во время первой и второй стимуляции TA-65® активность теломеразы во всех Т-лимфоцитах пациентов возрастала в 1,3 - 3,3 раза по сравнению с контролем.<br>
			• TA-65®, активирует теломеразу в CD4 и в CD8 Т-лимфоцитах через сигнальный путь MAPK.<br>
			<br>
			Публикация: 
			Функциональная оценка фармакологических активаторов теломеразы в Т-клетках человека. <a href="http://www.mdpi.com/2073-4409/2/1/57" target="_blank">Cell Journal. 2013 January 14: ISSN 2073-4409
