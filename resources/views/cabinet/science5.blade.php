			Актуальность проблемы: Теломераза является рибонуклеопротеином, который катализирует присоединение теломерных повторов к концам теломеры. 
			Теломеры - это длинные участки повторяющихся последовательностей нуклеотидов, которые покрывают концы хромосом и стабилизируют хромосому. 
			Ряд заболеваний связан с быстрой потерей теломер, проявляющейся преждевременным старением клеток. Потенциальный терапевтический эффект от 
			увеличения активности теломеразы включает, например, лечение ВИЧ-инфекции, которая характеризуется быстрым укорочением длины теломер и ранним 
			старением цитотоксических Т-лимфоцитов (CD8+ клетки) и снижением Т-хелперов.<br>
			<br>
			Цели: Изучить влияние активатора теломеразы на популяцию Т-хелперов у пациентов с ВИЧ-инфекцией и динамику вирусной нагрузки<br>
			<br>
			Методы: 3-м пациентам с ВИЧ-инфекцией был назначен активатор теломеразы ТА-65MD. 1-й пациент – женщина 50 лет с впервые выявленной ВИЧ-инфекцией, 
			лимфопенией, генерализованной лимфаденопатией, без ВААРТ, ТА-65 назначен в дозе 1 капсула в сутки (250 ед), получает в течение 5 месяцев. 
			2-й пациент – мужчина 34 года, ВИЧ+ в течение 10 лет, тяжелой лимфопенией, лихорадкой неясного генеза, без ВААРТ, ТА-65 получал в течение месяца, 
			самостоятельная отмена. 3-й пациент – мужчина 31 год, ВИЧ+ в течение 2-х лет, без ВААРТ, ТА-65 получает в суточной дозе 2 капсулы в сутки (500 ед) 
			в течение месяца.<br>
