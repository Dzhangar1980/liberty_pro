			<br><br>
			Шотландские медики установили, что ресвератрол, содержащийся в том числе в сухом красном вине, работает в качестве прекрасного терапевтического 
			препарата для блокировки воспалений, язвенных формирований и некоторых других опасных образований во внутренних органах человека. Шотландцы 
