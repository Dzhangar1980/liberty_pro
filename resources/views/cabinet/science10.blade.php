            Подобно всем полифенолам, Ресвератрол — мощный антиоксидант, активность которого превосходит витамин Е. Ресвератрол проявляет эстрогенную активность, 
			связываясь с теми же самыми рецепторами, что и эстрадиол, один из главных эстрогенов человека. Несмотря на то, что гормональная активность ресвератрола 
			значительно меньше, чем у эстрадиола (3-10 мкМ ресвератрола соответствует 0,1 нМ эстрадиола), в организме его может быть столько, что он будет действовать 
			даже сильнее, чем эстрадиол в физиологических концентрациях.
			<br><br>
			В отличие от эстрадиола и диэтилстилбэстрона, которые ускоряют развитие ряда гормонозависимых опухолей, и избыток которых связан с повышенным риском 
			развития рака молочных желез, ресвератрол проявляет разностороннее противоопухолевое действие. Эксперименты показывают, что он ингибирует 
			рибонуклеотид-редуктазу и подавляет синтез ДНК млекопитающих напрямую <b>блокируя размножение опухолевых клеток</b>.
			Другой механизм противоопухолевого действия ресвератрола заключается в конкуренции ресвератрола с эстрадиолом, за место связывания на рецепторе, 
			что снижает стимулирующее влияние эстрадиола на клетки гормонозависимых опухолей.
			<br><br>
			Как все фитоэстрогены, ресвератрол может уменьшать <b>риск остеопороза</b>, который часто бывает вызван снижением уровня эстрогенов у женщин в период менопаузы. 
			Так же ресвератрол <b>благотворно влияет на кожу</b>, стимулируя синтез коллагена и предотвращая его сшивки. Такой эффект объясняется сочетанным антиоксидантным 
